    public function logout() {
        auth()->logout();
        return redirect('/');
    }

    public function auth(Request $request) {
        $email = $request->email;
        $password = $request->password;

        $result = auth()->attempt(['email' => $email, 'password' => $password]);

        if($result) {
            return redirect('/');
        }
        else {
            return redirect()->back();
        }
    }

